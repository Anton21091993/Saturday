package Saturday;

public class SwitchClass {
    public static void main(String[] args) {
        int a =10;
        switch (a)
        {
            case 1:
                System.out.println("a=1");
                break;
                case 2:
                System.out.println("a=2");
                break;
            default:
                System.out.println("a not equals to 1 and 2");

        }
        System.out.println("So let's finish the work");
    }
}
